//! Accessing characters in a string

let str = 'Hello, World!';

console.log(str[0]); // H ანუ პირველი სიმბოლო ( ათვლა სტრინგებში იწყება 0-დან )

console.log(str[1]); // e ანუ მეორე სიმბოლო

console.log(str[2]); // l ანუ მესამე სიმბოლო

console.log(str[50]); // undefined ანუ არ არსებობს ასეთი ინდექსი

